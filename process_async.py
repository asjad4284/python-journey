import asyncio
from concurrent.futures import ProcessPoolExecutor

def encrypt(data):
    return f"{data}"

async def main():
    loop=asyncio.get_running_loop()
    with ProcessPoolExecutor() as pool:
        result=await loop.run_in_executor(pool,encrypt,"4234_1234_2345_0000")
        print(f"{result}")

if __name__=="__main__":
    asyncio.run(main())